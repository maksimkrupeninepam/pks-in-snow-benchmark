CREATE WAREHOUSE BENCHMARK_LOAD_WH_L WITH WAREHOUSE_SIZE = 'LARGE' WAREHOUSE_TYPE = 'STANDARD' AUTO_SUSPEND = 60 AUTO_RESUME = TRUE MIN_CLUSTER_COUNT = 1 MAX_CLUSTER_COUNT = 1 SCALING_POLICY = 'STANDARD';

use WAREHOUSE BENCHMARK_LOAD_WH_L;

-- ###### TPCH_SF1000000_ORIG - Original numeric keys
create schema DV_BENCHMARK.TPCH_SF1000_ORIG;

create table DV_BENCHMARK.TPCH_SF1000_ORIG.NATION as
select * from SNOWFLAKE_SAMPLE_DATA.TPCH_SF1000.NATION;

create table DV_BENCHMARK.TPCH_SF1000_ORIG.REGION as
select * from SNOWFLAKE_SAMPLE_DATA.TPCH_SF1000.REGION;

select 'CUSTOMER', getdate();

create table DV_BENCHMARK.TPCH_SF1000_ORIG.CUSTOMER as
select * from SNOWFLAKE_SAMPLE_DATA.TPCH_SF1000.CUSTOMER;

select 'SUPPLIER', getdate();

create table DV_BENCHMARK.TPCH_SF1000_ORIG.SUPPLIER as
select * from SNOWFLAKE_SAMPLE_DATA.TPCH_SF1000.SUPPLIER;

select 'PART', getdate();

create table DV_BENCHMARK.TPCH_SF1000_ORIG.PART as
select * from SNOWFLAKE_SAMPLE_DATA.TPCH_SF1000.PART;

select 'PARTSUPP', getdate();

create table DV_BENCHMARK.TPCH_SF1000_ORIG.PARTSUPP as
select * from SNOWFLAKE_SAMPLE_DATA.TPCH_SF1000.PARTSUPP;

select 'ORDERS', getdate();

create table DV_BENCHMARK.TPCH_SF1000_ORIG.ORDERS as
select * from SNOWFLAKE_SAMPLE_DATA.TPCH_SF1000.ORDERS order by O_ORDERDATE;

select 'LINEITEM', getdate();

create table DV_BENCHMARK.TPCH_SF1000_ORIG.LINEITEM as
select * from SNOWFLAKE_SAMPLE_DATA.TPCH_SF1000.LINEITEM order by L_SHIPDATE;

select 'STOP', getdate();

drop WAREHOUSE BENCHMARK_LOAD_WH_L;
